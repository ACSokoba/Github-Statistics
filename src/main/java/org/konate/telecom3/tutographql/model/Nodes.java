package org.konate.telecom3.tutographql.model;

import java.util.List;

public class Nodes {
	private List<Node> nodes;

	public Nodes() {
		super();
	}

	public Nodes(List<Node> nodes) {
		super();
		this.nodes = nodes;
	}

	public List<Node> getNodes() {
		return nodes;
	}

	public void setNodes(List<Node> nodes) {
		this.nodes = nodes;
	}
	
}
